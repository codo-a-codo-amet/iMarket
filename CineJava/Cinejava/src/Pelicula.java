public class Pelicula {
    private int id;
    private int Pelicula_details_id;
    private int Sala_id;
    private int Sala_Sala_id;
    private int Transaciones_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPelicula_details_id() {
        return Pelicula_details_id;
    }

    public void setPelicula_details_id(int Pelicula_details_id) {
        this.Pelicula_details_id = Pelicula_details_id;
    }

    public int getSala_id() {
        return Sala_id;
    }

    public void setSala_id(int Sala_id) {
        this.Sala_id = Sala_id;
    }

    public int getSala_Sala_id() {
        return Sala_Sala_id;
    }

    public void setSala_Sala_id(int Sala_Sala_id) {
        this.Sala_Sala_id = Sala_Sala_id;
    }

    public int getTransaciones_id() {
        return Transaciones_id;
    }

    public void setTransaciones_id(int Transaciones_id) {
        this.Transaciones_id = Transaciones_id;
    }
    
}
